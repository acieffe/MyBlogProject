        // GET: Posts/Edit/5
        public async Task<IActionResult> Edit(int? id)
        {
            if (id == null)
            {
                return NotFound();
            }

            var post = await _context.Posts.FindAsync(id);
            if (post == null)
            {
                return NotFound();
            }
            ViewData["BlogtId"] = new SelectList(_context.Blogs, "Id", "Name", post.BlogtId);
            return View(post);
        }

        // POST: Posts/Edit/5
